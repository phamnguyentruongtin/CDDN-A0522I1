package ss2loop;

import java.util.Scanner;

public class hienthicacloaihinh {
    public static void main(String[] args) {

        int a = 3;
        int b = 10;
        for (int i = 1; i <= a; i++) {
            for (int j = 1; j <= b; j++) {
                System.out.printf("*");
            }
            System.out.println(" ");
        }
    }


}


